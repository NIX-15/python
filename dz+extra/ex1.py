dct = {
	1: {
		1: 11,
		2: 12,
		3: 13,
	},
	2: {
		1: 21,
		2: 22,
		3: 23,
	},
	3: {
		1: 24,
		2: 25,
		3: 26,
	},
}
print(dct)
temp=0
for i in dct:
    for j in dct[i]:
        print(f"{dct[i][j]} значение из подсловаря {j} большого словаря {i}")
        temp+=dct[i][j]
print(f"Общая сумма элементов в словарях = {temp}")